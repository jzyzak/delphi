class TestCoreParity:
    """The evaluation-fit artifact and the core reader must agree exactly."""

    def test_fitted_artifact_matches_learned_calibration(self) -> None:
        from core.forecast.calibration import FrozenCalibration, apply_floor, calibrate

        for method in ("isotonic", "platt"):
            records = [
                ScoredRecord(question_id=f"q{i}", domain="d", probability=p, outcome=o)
                for i, (p, o) in enumerate(
                    [
                        (0.05, 0.0),
                        (0.15, 0.0),
                        (0.3, 0.0),
                        (0.45, 1.0),
                        (0.55, 0.0),
                        (0.7, 1.0),
                        (0.85, 1.0),
                        (0.95, 1.0),
                    ]
                )
            ]
            artifact = fit_calibration_artifact(records, method=method, min_fit_points=1)
            learned = FrozenCalibration.from_dict(artifact.to_dict())
            grid = [0.001, 0.05, 0.2, 0.35, 0.5, 0.65, 0.8, 0.95, 0.999]
            for p in grid:
                assert learned.apply(p) == pytest.approx(
                    artifact.recalibrator.apply(p), abs=1e-12
                ), (method, p)
                composed = apply_floor(
                    calibrate(learned.apply(p), alpha=learned.alpha), learned.floor
                )
                assert composed == pytest.approx(artifact.apply(p), abs=1e-12), (method, p)
